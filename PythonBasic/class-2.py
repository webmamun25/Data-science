# Arithmetic Operator
print(5+6)
print(5-6)
print(5*6)
print(5/6)
# integer division
print(5//6)
print(5%6)
print(5**6) #5^6

# Relational operator
print(4>5)
print(4<5)
print(4>=4)
print(4==4)
print(4!=4)

# logical operator
print(1 and 0)
print(1 or 0)
print(not 1)

# Bitwise and operator
print(2 & 3)
# 010 and 110 output is 2->010
#Bitwise or operator
print(2|3)
# Bitwise XOR
print(2^3)
# Bitwise not

print(~3)
# left shift
print(4 >> 2)
# right shift
print(5<<2)

# Assignement operator

a=2
a+=2
print(a)

# Membership operator

# in/not in
print('D' in "Delhi")
print(2 not in [2,3,4,5])